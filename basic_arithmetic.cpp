#include <iostream>
using namespace std;
  
int main() {
    int result_add = 20 + 5;
    int result_sub = 20 - 5;
    int result_mult = 20 * 5;
    int result_div =  20/5;

    cout << "Result of 20 + 5: " << result_add << endl;
    cout << "Result of 20 - 5: " << result_sub << endl;
    cout << "Result of 20 * 5: " << result_mult << endl;
    cout << "Result of 20 / 5: " << result_div << endl;
    return 0;
}